#Aquí está el resultado mental de cada operación:

#1. `print(a * 5)` - Resultado: 50 (10 multiplicado por 5 es 50).
   
#2. `print(a - b)` - Resultado: 15 (La resta de 10 menos -5 es 15).

#3. `print(c + "Mundo")` - Resultado: "Hola Mundo" (La concatenación de "Hola " con "Mundo" da como resultado "Hola Mundo").

#4. `print(c * 2)` - Resultado: "Hola Hola " (La cadena "Hola " se repite dos veces).

#5. `print(d[-1])` - Resultado: 3 (Obtiene el último elemento de la lista `d`, que es 3).

#6. `print(d[1:])` - Resultado: [2, 3] (Obtiene una porción de la lista `d`, desde el segundo elemento hasta el último).

#7. `print(d + d)` - Resultado: [1, 2, 3, 1, 2, 3] (Concatenación de la lista `d` consigo misma).